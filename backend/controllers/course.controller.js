const geminiService = require('../../ai-engine/gemini.service');
const youtubeService = require('../services/youtube.service');
const db = require('../services/db.service');

exports.createCourse = async (req, res) => {
  try {
    const { topic, difficulty, userId, save = true } = req.body;
    console.log(`[Course] Received request - Topic: "${topic}", Difficulty: "${difficulty}", User: ${userId || 'anonymous'}, Save: ${save}`);
    
    if (!topic) {
      console.log('[Course] Error: Topic is required but not provided');
      return res.status(400).json({ error: 'Topic is required' });
    }

    console.log(`[Course] Generating full course for: "${topic}" (User: ${userId || 'anonymous'})`);

    // 1. Generate full course content using Gemini
    let fullCourse;
    try {
      fullCourse = await geminiService.generateFullCourse({ topic, difficulty });
      console.log(`[Course] Successfully generated course structure with ${fullCourse.modules?.length || 0} modules`);
    } catch (geminiError) {
      console.error(`[Course] Gemini generation failed:`, geminiError.message);
      if (geminiError.message?.includes('GEMINI_API_KEY')) {
        return res.status(500).json({ error: 'AI Service configuration error. Please check API key configuration.' });
      }
      if (geminiError.status === 429) {
        return res.status(429).json({ error: 'AI provider is busy. Please try again in a few minutes.' });
      }
      throw geminiError;
    }

    if (!fullCourse.modules || !Array.isArray(fullCourse.modules)) {
      console.error('[Course] Error: Invalid course structure generated by AI');
      throw new Error('Invalid course structure generated by AI');
    }

    const courseId = fullCourse.id;
    console.log(`[Course] Generated course ID: ${courseId}`);

    // 2. [NEW] Enrich each lesson with YouTube suggestions in parallel
    console.log(`[Course] Enriching ${courseId} with YouTube resources...`);
    const enrichmentPromises = [];
    
    for (const module of fullCourse.modules) {
      for (const lesson of module.lessons) {
        enrichmentPromises.push((async () => {
          const query = `${fullCourse.title} ${lesson.title} educational`;
          try {
            lesson.youtubeSuggestions = await youtubeService.searchVideos(query, 2);
            console.log(`[Course] YouTube suggestions added for lesson: ${lesson.title}`);
          } catch (youtubeError) {
            console.warn(`[Course] YouTube enrichment failed for lesson "${lesson.title}":`, youtubeError.message);
            lesson.youtubeSuggestions = [];
          }
        })());
      }
    }
    
    try {
      await Promise.all(enrichmentPromises);
      console.log(`[Course] YouTube enrichment completed for all lessons`);
    } catch (enrichmentError) {
      console.warn('[Course] Some YouTube enrichments failed, continuing with course generation');
    }

    // Prepare structure for UI consistency
    const courseData = {
      ...fullCourse,
      id: courseId,
      progress: 0,
      status: 'not-started',
      category: 'AI & Learning'
    };

    // 3. Store in Firestore (Admin SDK) - ONLY if save is true
    if (save) {
      console.log(`[Course] Attempting to persist course "${courseId}" for user: ${userId || 'anonymous'}`);
      try {
        if (userId) {
          await db.save('users', userId, { lastActive: new Date().toISOString() }, 'courses', courseId);
          await db.save('users', userId, courseData, 'courses', courseId);
          console.log(`[Course] Successfully saved course to user collection: ${userId}/courses/${courseId}`);
        } else {
          await db.save('courses', courseId, courseData);
          console.log(`[Course] Successfully saved course to global collection: courses/${courseId}`);
        }
      } catch (dbErr) {
        console.error(`[Course] Firestore persistence failed for "${courseId}":`, dbErr.message);
        console.log('[Course] Continuing with course generation despite storage failure');
      }
    } else {
      console.log(`[Course] Skipping persistence for "${courseId}" as save=false`);
    }

    console.log(`[Course] Course generation completed successfully: ${courseId}`);
    res.status(200).json({ courseId, course: courseData });
  } catch (error) {
    console.error('[Course] Course generation failed with error:', error);
    console.error('[Course] Error stack:', error.stack);
    
    if (error.message?.includes('GEMINI_API_KEY')) {
      return res.status(500).json({ error: 'AI Service configuration error. Please check your Gemini API key configuration.' });
    }
    if (error.status === 429) {
      return res.status(429).json({ error: 'AI provider is busy. Please try again in a few minutes.' });
    }
    if (error.message?.includes('Invalid course structure')) {
      return res.status(500).json({ error: 'AI generated invalid course structure. Please try again with a different topic.' });
    }
    
    res.status(500).json({ 
      error: 'An unexpected error occurred during course generation.', 
      detail: error.message,
      suggestion: 'Please try again with a different topic or check your API configuration'
    });
  }
};

exports.saveCourse = async (req, res) => {
  try {
    const { userId, courseId, courseData } = req.body;
    
    if (!courseId || !courseData) {
      return res.status(400).json({ error: 'courseId and courseData are required' });
    }

    console.log(`[Course] Persisting course "${courseId}" for user: ${userId || 'anonymous'}`);
    
    if (userId) {
      await db.save('users', userId, { lastActive: new Date().toISOString() }, 'courses', courseId);
      await db.save('users', userId, courseData, 'courses', courseId);
    } else {
      await db.save('courses', courseId, courseData);
    }

    res.status(200).json({ success: true, courseId });
  } catch (error) {
    console.error(`[Course] Save failed:`, error);
    res.status(500).json({ error: error.message });
  }
};

exports.getCourse = async (req, res) => {
  try {
    const courseId = req.params.id;
    const userId = req.query.userId;
    
    console.log(`[Course] Getting course: ${courseId} for user: ${userId || 'anonymous'}`);
    
    let course = null;
    
    // 1. Try to get from user collection if userId is provided
    if (userId && userId !== "null" && userId !== "undefined") {
      console.log(`[Course] Trying to get course from user collection: users/${userId}/courses/${courseId}`);
      course = await db.get('users', userId, 'courses', courseId);
    }
    
    // 2. Fallback: try top-level courses collection
    if (!course) {
      console.log(`[Course] Trying to get course from global collection: courses/${courseId}`);
      course = await db.get('courses', courseId);
    }
    
    if (!course) {
      console.log(`[Course] Course not found: ${courseId}`);
      return res.status(404).json({ error: 'Course not found' });
    }
    
    console.log(`[Course] Successfully retrieved course: ${courseId}`);
    res.status(200).json(course);
  } catch (error) {
    console.error(`[Course] Error getting course:`, error);
    res.status(500).json({ error: error.message });
  }
};

exports.listCourses = async (req, res) => {
  try {
    const coursesRaw = await db.list('courses');
    const courses = coursesRaw.map(c => ({
      id: c.id,
      title: c.title,
      topic: c.topic,
      difficulty: c.difficulty,
      description: c.description,
      createdAt: c.createdAt,
      lessonCount: c.modules?.reduce((acc, m) => acc + (m.lessons?.length || 0), 0) || 0,
    }));
    res.status(200).json({ courses });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
};
