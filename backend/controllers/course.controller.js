const geminiService = require('../../ai-engine/gemini.service');
const videoService = require('../services/video.service');
const db = require('../services/db.service');

exports.createCourse = async (req, res) => {
  try {
    const { topic, difficulty, userId } = req.body;
    if (!topic) return res.status(400).json({ error: 'Topic is required' });

    console.log(`[Course] Generating full course for: "${topic}" (User: ${userId || 'anonymous'})`);

    // Generate full course content using Gemini
    const fullCourse = await geminiService.generateFullCourse({ topic, difficulty });
    const courseId = fullCourse.id;

    if (!fullCourse.modules || !Array.isArray(fullCourse.modules)) {
      throw new Error('Invalid course structure generated by AI');
    }

    // Prepare structure for UI consistency
    const courseData = {
      ...fullCourse,
      id: courseId,
      progress: 0,
      status: 'not-started',
      category: 'AI & Learning'
    };

    // Store in Firestore (Admin SDK)
    try {
      if (userId) {
        // Nested under /users/uid/courses/id
        await db.save('users', userId, { lastActive: new Date().toISOString() }, 'courses', courseId);
        await db.save('users', userId, courseData, 'courses', courseId);
      } else {
        // Global collection
        await db.save('courses', courseId, courseData);
      }
      console.log(`[Course] Successfully persisted course "${courseId}"`);
    } catch (dbErr) {
      console.error(`[Course] Firestore persistence failed for "${courseId}":`, dbErr.message);
    }

    res.status(200).json({ courseId, course: courseData });
  } catch (error) {
    if (error.message?.includes('GEMINI_API_KEY')) {
      console.error('[Course] Critical Config Error:', error.message);
      return res.status(500).json({ error: 'AI Service configuration error' });
    }
    if (error.status === 429) {
      console.warn('[Course] Rate limited by Gemini API');
      return res.status(429).json({ error: 'AI provider is busy. Please try again in a few minutes.' });
    }

    console.error('[Course] Generation failed:', error);
    res.status(500).json({ error: 'An unexpected error occurred during course generation.', detail: error.message });
  }
};

exports.getCourse = async (req, res) => {
  try {
    const courseId = req.params.id;
    const course = await db.get('courses', courseId);
    if (!course) return res.status(404).json({ error: 'Course not found' });
    res.status(200).json(course);
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
};

exports.listCourses = async (req, res) => {
  try {
    const coursesRaw = await db.list('courses');
    const courses = coursesRaw.map(c => ({
      id: c.id,
      title: c.title,
      topic: c.topic,
      difficulty: c.difficulty,
      description: c.description,
      createdAt: c.createdAt,
      lessonCount: c.modules?.reduce((acc, m) => acc + (m.lessons?.length || 0), 0) || 0,
    }));
    res.status(200).json({ courses });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
};
